#include "libft.h"

void	ft_sort_int_tab(int *tab, int size)
{
	int i;

	i = 0;
	if (tab)
		while (i < size)
		{
			if (tab[i] > tab[i + 1])
			{
				ft_swap(&tab[i], &tab[i + 1]);
				i = -1;
			}
			i++;
		}
}
